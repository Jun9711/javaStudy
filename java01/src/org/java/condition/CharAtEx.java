package org.java.condition;

public class CharAtEx {

	public static void main(String[] args) {
		
		
		System.out.println("java2022".charAt(0)); //문자열 0번지
		System.out.println("java2022".charAt(1)); //문자열 1번지
		System.out.println("java2022".charAt(2)); //문자열 2번지
		System.out.println("java2022".charAt(3)); //문자열 3번지
		System.out.println("java2022".charAt(4)); //문자열 4번지
	}
}
